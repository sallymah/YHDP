package tw.com.hyweb.svc.yhdp.batch.impfiles.ImpMPMerch;

public class MPMerchBean {

	String merchId = "";
	String merchLocName = "";
	String tel = "";
	String fax = "";
	String zipCode = "";
	String address = "";
	String status = "";
	String effectiveDate = "";
	String terminationDate = "";
	String ecaMerchId = "";
	String realEcaMerchId = "";
	
	public String getMerchId() {
		return merchId;
	}

	public void setMerchId(String merchId) {
		this.merchId = merchId;
	}

	public String getMerchLocName() {
		return merchLocName;
	}

	public void setMerchLocName(String merchLocName) {
		this.merchLocName = merchLocName;
	}

	public String getTel() {
		return tel;
	}

	public void setTel(String tel) {
		this.tel = tel;
	}

	public String getFax() {
		return fax;
	}

	public void setFax(String fax) {
		this.fax = fax;
	}

	public String getZipCode() {
		return zipCode;
	}

	public void setZipCode(String zipCode) {
		this.zipCode = zipCode;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getEffectiveDate() {
		return effectiveDate;
	}

	public void setEffectiveDate(String effectiveDate) {
		this.effectiveDate = effectiveDate;
	}

	public String getTerminationDate() {
		return terminationDate;
	}

	public void setTerminationDate(String terminationDate) {
		this.terminationDate = terminationDate;
	}

	public String getEcaMerchId() {
		return ecaMerchId;
	}

	public void setEcaMerchId(String ecaMerchId) {
		this.ecaMerchId = ecaMerchId;
	}

	public String getRealEcaMerchId() {
		return realEcaMerchId;
	}

	public void setRealEcaMerchId(String realEcaMerchId) {
		this.realEcaMerchId = realEcaMerchId;
	}

	public String toString() 
	{
		return "[MPMerchBean] " + 
				"merchId = " + getMerchId() + ", " + 
				"merchLocName = " + getMerchLocName() + ", " + 
				"tel = " + getTel() + ", " + 
				"fax = " + getFax() + ", " + 
				"zipCode = " + getZipCode() + ", " + 
				"address = " + getAddress() + ", " + 
				"status = " + getStatus() + ", " + 
				"effectiveDate = " + getEffectiveDate() + ", " +
				"terminationDate = " + getTerminationDate() + ", " + 
				"ecaMerchId = " + getEcaMerchId() + ", " + 
				"realEcaMerchId = " + getRealEcaMerchId() + ". ";
	}
	
}
